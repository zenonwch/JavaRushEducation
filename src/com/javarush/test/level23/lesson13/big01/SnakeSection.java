package com.javarush.test.level23.lesson13.big01;

public class SnakeSection {
	private int x;
	private int y;

	public SnakeSection(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
